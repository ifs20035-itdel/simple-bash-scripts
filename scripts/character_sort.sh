#!/bin/bash

# This command below will print 1 to 10 with each of the iteration increment 2 from the last 
echo {1..10..2}

# This command below will print A to Z in alphabetical order.

echo {A..Z}

# This command below will print a to Z in the ASCII table.
echo {a..Z}

# This command below will print A to z with the sign_character 
echo {A..z}
